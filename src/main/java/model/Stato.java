package model;

/**
 * L'enumerazione Stato.
 */
public enum Stato {
    /**
     * Informa che lo stato sia stato approvato.
     */
    Approvata,
    /**
     * Informa che lo stato sia in fase di risposta.
     */
    InAttesa,
    /**
     * Informa che lo stato e' stato rifiutato.
     */
    Rifiutata;
}
